# ekspresi menurut tipe data yang dihasilkan
# Ekspresi Aritmetika <numerik> <operator> <numerik> = <numerik>:
print(2 + 2)
print(2 * 2)

# Ekspresi Relasional <numerik> <operator> <numerik> = <boolean>:
print(3 < 10)
print(1 > 10)

# Ekspresi Logika <boolean> <operator> <boolean> = <boolean>:
print(True and False)
